export function Sidebar() {
  const { isSyncing, activeSync } = useSync();
  const syncPercentage = activeSync
    ? Math.round((activeSync.progress.current / activeSync.progress.total) * 100)
    : 0;

  return (
    <motion.aside
      initial={{ x: -20, opacity: 0 }}
      animate={{ x: 0, opacity: 1 }}
      transition={{ duration: 0.3 }}
      className="w-64 bg-secondary-800 border-r border-secondary-700 flex flex-col h-screen"
    >
      {/* Logo/Brand */}
      <div className="p-6 border-b border-secondary-700">
        <motion.h1
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.1 }}
          className="text-xl font-bold text-primary-400"
        >
          Horus Attendance
        </motion.h1>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-1">
        {navItems.map((item, index) => (
          <motion.div
            key={item.path}
            initial={{ x: -20, opacity: 0 }}
            animate={{ x: 0, opacity: 1 }}
            transition={{ delay: 0.05 * index }}
          >
            <NavLink
              to={item.path}
              className={({ isActive }) =>
                `flex items-center gap-3 px-4 py-3 rounded-lg transition-colors duration-200 ${
                  isActive
                    ? 'bg-primary-600 text-white'
                    : 'text-secondary-300 hover:bg-secondary-700 hover:text-white'
                }`
              }
            >
              {item.icon}
              <span className="font-medium">{item.label}</span>
              {/* Show sync indicator badge on the Sync nav item */}
              {item.path === '/sync' && isSyncing && (
                <span className="ml-auto flex items-center gap-1 px-2 py-0.5 bg-primary-600/20 text-primary-400 text-xs rounded-full">
                  <motion.div
                    animate={{ rotate: 360 }}
                    transition={{ duration: 1.5, repeat: Infinity, ease: 'linear' }}
                    className="w-2.5 h-2.5 border border-primary-400 border-t-transparent rounded-full"
                  />
                  {syncPercentage}%
                </span>
              )}
            </NavLink>
          </motion.div>
        ))}
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-secondary-700">
        <p className="text-xs text-secondary-500 text-center">
          v0.1.0
        </p>
      </div>
    </motion.aside>
  );
}
